# ║  КРОК 5: HMAC — підпис повідомлень                            ║
# ══════════════════════════════════════════════════════════════════

def step5_hmac():
    """
    HMAC = Hash-based Message Authentication Code.

    Хеш сам по собі не доводить, що повідомлення прийшло від конкретного відправника.
    Зловмисник міг перехопити повідомлення, змінити його, і просто перерахувати хеш.

    HMAC додає секретний ключ:
        HMAC(key, message) = хеш, який можна обчислити ТІЛЬКИ маючи key.

    Куди застосовується:
      • Підпис вебхуків (GitHub/Stripe підписують payload своїм ключем).
      • Cookie-сесії: сервер підписує сесію, щоб юзер не міг її підробити.
      • API: підпис кожного запиту, щоб довести "це справді від мене".
    """
    section("КРОК 5: HMAC — підпис повідомлень")

    # ── 5.1: Простий приклад ──────────────────────────────────────
    # Уявімо, що ми хочемо передати повідомлення з боту в зовнішній сервіс,
    # і отримувач має пересвідчитись, що ніхто його не змінив у дорозі.
    secret_key = b"my-super-secret-key"
    message = b"user_id=458192038&city=Kyiv&temp=12.5"

    # hmac.new(key, msg, digestmod=sha256) → об'єкт HMAC.
    # .hexdigest() → підпис у вигляді hex-рядка.
    signature = hmac.new(secret_key, message, hashlib.sha256).hexdigest()
    print(f"  message:   {message.decode()}")
    print(f"  HMAC-key: <не показуємо в реальному коді>")
    print(f"  signature: {signature}\n")

    # ── 5.2: Перевірка на стороні отримувача ──────────────────────
    # Отримувач знає той самий ключ. Обчислює свій HMAC і порівнює.
    # ВАЖЛИВО: порівнювати треба через hmac.compare_digest(), а не "==".
    # Чому? Звичайне порівняння рядків може "піддатись" timing attack:
    # мікросекундна різниця в часі видасть, скільки літер уже збіглось.
    # compare_digest() порівнює за константний час.
    incoming_sig = signature  # отримали по мережі
    valid = hmac.compare_digest(
        signature.encode(),
        incoming_sig.encode(),
    )
    print(f"  Перевірка HMAC: {valid}\n")

    # ── 5.3: Що буде, якщо змінити повідомлення ───────────────────
    tampered = b"user_id=458192038&city=Kyiv&temp=99.9"  # хтось підробив температуру
    new_sig = hmac.new(secret_key, tampered, hashlib.sha256).hexdigest()
    print(f"  Якщо повідомлення підмінили, без ключа підпис не зробиш:")
    print(f"    оригінал: {signature[:32]}...")
    print(f"    підробка: {new_sig[:32]}...")
    print(f"  Відрізняються: {signature != new_sig}\n")

    print("  💡 HMAC — це 'хеш + секрет'. Без секрету не зробиш правильний підпис.\n")


