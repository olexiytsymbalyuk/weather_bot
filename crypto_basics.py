# ║  КРОК 4: bcrypt — правильно для паролів                       ║
# ══════════════════════════════════════════════════════════════════

def step4_bcrypt():
    """
    bcrypt — стандарт для хешування паролів. Що він робить інакше:

      • ВБУДОВАНА сіль   — bcrypt сам генерує і зберігає сіль у хеші.
                            Тобі не треба окремо керувати сіллю.
      • ПОВІЛЬНИЙ        — у 100000+ разів повільніший за SHA-256.
                            Це фіча: легальний логін — 100мс, атака — століття.
      • COST FACTOR      — параметр складності (4..15). Кожен +1 = у 2 рази повільніше.
                            Стандартна рекомендація 2026 — cost=12.

    Альтернативи: argon2 (виграв конкурс Password Hashing Competition 2015),
                   scrypt. Усі вони "повільні" за дизайном.
    """
    section("КРОК 4: bcrypt — правильний хеш для паролів")

    password = "qwerty123"

    # ── 4.1: Хешування ────────────────────────────────────────────
    # bcrypt працює з bytes (не str).
    # bcrypt.gensalt(rounds=12) — генерує сіль + параметри.
    # bcrypt.hashpw(password, salt) — повертає байти, що містять і сіль, і хеш.
    salt = bcrypt.gensalt(rounds=12)
    t0 = time.perf_counter()
    hashed = bcrypt.hashpw(password.encode(), salt)
    elapsed = time.perf_counter() - t0
    print(f"  bcrypt.hashpw('{password}', salt) →")
    print(f"    {hashed.decode()}")
    print(f"    Час обчислення (cost=12): {elapsed * 1000:.1f} мс")
    print(f"    Зверни увагу: у самому хеші вже зашиті cost і сіль.\n")

    # ── 4.2: Перевірка ────────────────────────────────────────────
    # bcrypt.checkpw — сам витягне сіль/cost з hashed і обчислить хеш заново.
    ok = bcrypt.checkpw(password.encode(), hashed)
    wrong = bcrypt.checkpw(b"qwerty1234", hashed)
    print(f"  bcrypt.checkpw — правильний пароль: {ok}")
    print(f"  bcrypt.checkpw — НЕправильний пароль: {wrong}\n")

    # ── 4.3: Демо cost factor ─────────────────────────────────────
    # Показуємо: збільшення cost на 2 → у 4 рази повільніше.
    print("  Залежність часу від cost factor (один пароль):")
    for cost in (4, 8, 12):
        s = bcrypt.gensalt(rounds=cost)
        t0 = time.perf_counter()
        bcrypt.hashpw(password.encode(), s)
        dt = (time.perf_counter() - t0) * 1000
        print(f"    cost={cost:2d}: {dt:8.2f} мс")
    print()

    print("  💡 Для паролів — bcrypt (або argon2). Усе. Не SHA-256, не MD5.\n")


