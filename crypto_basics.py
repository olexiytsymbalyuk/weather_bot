def practical_examples():
    """
    Демо двох сценаріїв з нашого бота:

      1) Хешування user_id перед записом у bot_log.csv.
      2) Шифрування WEATHER_API_KEY для зберігання у вигляді шифру.

    Сам бот переключиться на ці варіанти у наступному кроці (зміни в bot.py).
    """
    section("ПРАКТИКА: як це інтегрується в бота")

    # ── 1) Хешування user_id для логів ────────────────────────────
    salt = b"58c1d2e3f4567890abcdef0123456789"  # у реальному коді — з .env
    user_id = 458192038

    def hash_user_id(uid: int, salt: bytes) -> str:
        """Те саме, що піде в bot.log_request() після уроку 5."""
        return hashlib.sha256(salt + str(uid).encode()).hexdigest()

    hashed = hash_user_id(user_id, salt)
    print("  1) Хешування user_id для bot_log.csv:")
    print(f"     plain user_id (як було):     {user_id}")
    print(f"     hashed (як буде в логах):    {hashed}")
    print(f"     (sha256 з сіллю, salt у .env, у git не потрапляє)\n")

    # /stats у боті використовує df["user_id"].nunique().
    # Хеш стабільний → однакові user_id → однакові хеші → nunique() працює.
    print("     /stats далі рахує 'унікальних користувачів' правильно,")
    print("     бо однаковий user_id дає однаковий хеш — nunique() це бачить.\n")

    # ── 2) Шифрування API-ключа ──────────────────────────────────
    # На демо-цілях генеруємо ключ тут; у реальному коді — з .env.
    key = Fernet.generate_key()
    f = Fernet(key)
    api_key_plain = "abc123-real-openweather-key-456"
    api_key_token = f.encrypt(api_key_plain.encode())

    print("  2) Шифрування WEATHER_API_KEY:")
    print(f"     plaintext (як було в .env):  {api_key_plain}")
    print(f"     encrypted (як буде в .env):  {api_key_token.decode()[:50]}...")
    print(f"     При старті бот робить Fernet(FERNET_KEY).decrypt(...) → отримує plain.\n")

    print("  💡 Питання учневі: чому ми ХЕШУЄМО user_id, а ШИФРУЄМО API-ключ?")
    print("     Відповідь: ми НЕ хочемо ніколи знати user_id назад (приватність),")
    print("                 але хочемо знати API-ключ назад (треба ходити в API).\n")


