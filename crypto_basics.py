# ║  КРОК 6: Симетричне шифрування (Fernet)                       ║
# ══════════════════════════════════════════════════════════════════

def step6_symmetric_encryption():
    """
    ХЕШУВАННЯ ≠ ШИФРУВАННЯ. Принципова різниця:

        ХЕШ          — одностороннє. Назад НЕ повернеш.
        ШИФРУВАННЯ   — двостороннє. Можна зашифрувати І розшифрувати назад.

    Симетричне шифрування — той самий ключ і для шифрування, і для розшифровки.
    (На відміну від асиметричного, де є пара public/private — не в цьому уроці.)

    Fernet (з бібліотеки cryptography) — це "батарейки включені":
      • AES-128 у режимі CBC для шифрування,
      • HMAC-SHA-256 для перевірки цілісності,
      • timestamp у вкладеному форматі,
      • base64-кодування у вихід.

    Усе зашите в один токен. Тобі не треба знати, що таке "режим CBC".
    """
    section("КРОК 6: Симетричне шифрування (Fernet)")

    # ── 6.1: Генерація ключа ──────────────────────────────────────
    # Fernet.generate_key() → 32-байтовий ключ у base64 (44 символи).
    # Це ТВІЙ секрет. Хто має цей ключ, може все розшифрувати.
    key = Fernet.generate_key()
    print(f"  Згенерований ключ (зберігати в .env, НЕ в коді):")
    print(f"    {key.decode()}\n")

    # ── 6.2: Шифруємо та розшифровуємо ────────────────────────────
    f = Fernet(key)
    plaintext = b"WEATHER_API_KEY=abc123def456"
    token = f.encrypt(plaintext)
    print(f"  plaintext:  {plaintext.decode()}")
    print(f"  encrypted:  {token.decode()}\n")

    decrypted = f.decrypt(token)
    print(f"  decrypted:  {decrypted.decode()}")
    print(f"  Збігається з оригіналом: {plaintext == decrypted}\n")

    # ── 6.3: Кожен запуск encrypt() дає РІЗНИЙ токен ──────────────
    # Fernet додає випадковий IV (initialization vector) — і кожен раз шифр
    # виглядає по-новому, навіть якщо вхід той самий. Це ВАЖЛИВО: інакше
    # атакувальник, що бачить мережевий трафік, помічає, коли передається
    # та сама команда (бо шифр однаковий).
    token2 = f.encrypt(plaintext)
    print(f"  Той самий plaintext → інший шифр (через випадковий IV):")
    print(f"    спроба 1: {token.decode()[:40]}...")
    print(f"    спроба 2: {token2.decode()[:40]}...")
    print(f"  Шифри різні: {token != token2}, але обидва розшифруються коректно.\n")

    # ── 6.4: Цілісність — підробка не пройде ──────────────────────
    # Fernet перевіряє HMAC при decrypt. Якщо змінити хоч 1 байт токена —
    # decrypt() кине InvalidToken. Це захищає від підміни шифру у дорозі.
    tampered = token[:-2] + b"AA"  # змінили останні 2 символи
    try:
        f.decrypt(tampered)
        print("  Підробка пройшла — НЕ повинно бути!")
    except Exception as e:
        print(f"  Спроба розшифрувати підробку → {type(e).__name__}: відкинуто.")
    print()

    print("  💡 Симетричне шифрування — для секретів, які потім треба розшифрувати.")
    print("     Для паролів — НЕ використовуй: тобі не треба знати пароль користувача,")
    print("     тобі треба перевірити його. Для перевірки — bcrypt.\n")


