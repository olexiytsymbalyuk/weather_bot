# ║  КРОК 3: SALT — навіщо солити паролі                          ║
# ══════════════════════════════════════════════════════════════════

def step3_salt():
    """
    Salt (сіль) — випадкові байти, які домішуються до пароля ПЕРЕД хешуванням.

    Два юзери з паролем "qwerty" → два РІЗНИХ хеші, бо солі різні.
    Атакувальник вже не може мати ОДНУ rainbow-table на всіх — для кожного
    юзера треба будувати окремо. Це багатократно дорожче.

    Правила:
      • Сіль має бути ВИПАДКОВОЮ (secrets.token_bytes, НЕ random).
      • Сіль має бути УНІКАЛЬНОЮ для кожного юзера / запису.
      • Сіль не секрет — її можна зберігати поряд з хешем у БД.
      • Довжина — мінімум 16 байт.
    """
    section("КРОК 3: Salt — навіщо солити паролі")

    # ── 3.1: Два юзери з ОДНАКОВИМ паролем → різні хеші ───────────
    password = "qwerty"

    # secrets.token_bytes(N) — криптографічно стійкі N випадкових байтів.
    # На відміну від random.randbytes — не предсказувані, придатні для крипти.
    salt_alice = secrets.token_bytes(16)
    salt_bob = secrets.token_bytes(16)

    # "Посолити" — конкатенація байтів солі та пароля перед хешуванням.
    hash_alice = hashlib.sha256(salt_alice + password.encode()).hexdigest()
    hash_bob = hashlib.sha256(salt_bob + password.encode()).hexdigest()

    print(f"  Аліса й Боб мають однаковий пароль '{password}'.")
    print(f"  Але солі різні → і хеші різні:")
    print(f"    Аліса: salt={salt_alice.hex()[:16]}... hash={hash_alice[:16]}...")
    print(f"    Боб:   salt={salt_bob.hex()[:16]}... hash={hash_bob[:16]}...")
    print(f"  Хеші різні: {hash_alice != hash_bob}\n")

    # ── 3.2: Перевірка пароля при логіні ──────────────────────────
    # У БД зберігаємо salt + hash. При логіні:
    #   1) Беремо salt юзера з БД.
    #   2) Хешуємо salt + введений_пароль.
    #   3) Порівнюємо результат з хешем у БД.
    incoming_password = "qwerty"  # юзер ввів свій пароль
    check = hashlib.sha256(salt_alice + incoming_password.encode()).hexdigest()
    print(f"  Логін Аліси (вводить '{incoming_password}'):")
    print(f"    обчислений хеш збігається з БД? {check == hash_alice}\n")

    incoming_wrong = "qwerty!"
    check_wrong = hashlib.sha256(salt_alice + incoming_wrong.encode()).hexdigest()
    print(f"  Логін Аліси (вводить '{incoming_wrong}'):")
    print(f"    збігається? {check_wrong == hash_alice}\n")

    print("  💡 Сіль усуває rainbow-table. Але хеш все ще ШВИДКИЙ —")
    print("     перебір 'qwerty', 'password', '123456' з відомою сіллю —")
    print("     години роботи GPU. Для паролів треба ПОВІЛЬНИЙ хеш → bcrypt.\n")


