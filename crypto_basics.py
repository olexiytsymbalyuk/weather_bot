# ║  КРОК 2: MD5 і SHA-1 — АНТИПАТЕРН (чому НЕ використовуємо)    ║
# ══════════════════════════════════════════════════════════════════

def step2_legacy_hashes():
    """
    MD5 (1991) і SHA-1 (1995) — старі хеш-функції. Усе ще працюють у коді,
    бо backwards-compatibility, але для криптографії — їх не використовують:

      • MD5:   знайдені РЕАЛЬНІ колізії (різні входи → той самий хеш).
               Зламано в 2004-2008 роках. Швидкість — мільярди MD5/сек на GPU.
      • SHA-1: формально зламано в 2017 (Google SHAttered).
               Великі сервіси (Git, TLS-сертифікати) поступово відмовляються.

    Де ще зустрічаються:
      • MD5 — як швидкий "контрольний код" для файлів, де колізії не страшні.
      • SHA-1 — у старіших Git-репозиторіях для хешу комітів (мігрують на SHA-256).

    Висновок: для НОВОГО коду — тільки SHA-256 і новіші (SHA-3, BLAKE2/3).
    """
    section("КРОК 2: MD5 і SHA-1 — антипатерн для безпеки")

    password = "qwerty123"
    md5 = hashlib.md5(password.encode()).hexdigest()
    sha1 = hashlib.sha1(password.encode()).hexdigest()
    sha256 = hashlib.sha256(password.encode()).hexdigest()

    print(f"  Той самий пароль '{password}', різні хеші:")
    print(f"    MD5    ({len(md5):3d} симв.): {md5}")
    print(f"    SHA-1  ({len(sha1):3d} симв.): {sha1}")
    print(f"    SHA-256({len(sha256):3d} симв.): {sha256}\n")

    # ── 2.1: Чому самого хешу замало для паролів ──────────────────
    # Уяви, що ти зловив дамп БД з SHA-256-хешами паролів.
    # Сучасний GPU може рахувати мільярди SHA-256 на секунду.
    # Атакувальник тупо хешує "123456", "password", "qwerty", і так далі —
    # це rainbow-table атака.
    common = ["123456", "password", "qwerty", "qwerty123", "admin"]
    rainbow = {hashlib.sha256(p.encode()).hexdigest(): p for p in common}
    print("  Демо rainbow-table (заздалегідь обчислені хеші):")
    target_hash = sha256
    if target_hash in rainbow:
        print(f"    Атакувальник побачив хеш {target_hash[:16]}...")
        print(f"    Знайшов у rainbow → пароль: '{rainbow[target_hash]}'")
        print(f"    Розшифрування — миттєве, без перебору.\n")

    print("  💡 Не зберігай паролі як sha256(password). Це наївно й погано.")
    print("     Рішення: 1) сіль (КРОК 3), 2) повільні алгоритми (bcrypt — КРОК 4).\n")


