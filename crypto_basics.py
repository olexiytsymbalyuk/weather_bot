# ══════════════════════════════════════════════════════════════════
# ║  КРОК 1: Що таке хеш-функція                                  ║
# ══════════════════════════════════════════════════════════════════

def step1_what_is_hash():
    """
    Хеш-функція бере вхід будь-якої довжини й повертає вихід ФІКСОВАНОЇ довжини.

    Властивості "хорошого" криптографічного хешу:
      1) ДЕТЕРМІНОВАНІСТЬ — для однакового входу завжди той самий вихід.
      2) ОДНОСТОРОННІСТЬ  — за виходом не можна знайти вхід (тільки перебір).
      3) ЛАВИННИЙ ЕФЕКТ   — одна змінена літера → зовсім інший хеш.
      4) ШВИДКІСТЬ        — обчислюється швидко.
                            (для паролів швидкість — недолік, побачимо в bcrypt.)

    Найпоширеніший сучасний — SHA-256 (видає 256 біт = 64 hex-символи).
    """
    section("КРОК 1: Що таке хеш-функція (SHA-256 через hashlib)")

    # ── 1.1: Базовий хеш ──────────────────────────────────────────
    # hashlib.sha256() очікує bytes (НЕ str). Тому .encode("utf-8").
    # .hexdigest() — повертає хеш як рядок з hex-символів (зручно для логів/БД).
    text = "Привіт, світе"
    h = hashlib.sha256(text.encode("utf-8")).hexdigest()
    print(f"  SHA-256('{text}') =")
    print(f"    {h}")
    print(f"    (довжина: {len(h)} символів = 256 біт)\n")

    # ── 1.2: Детермінованість ─────────────────────────────────────
    # Той самий вхід → ТОЧНО той самий хеш. Завжди. На будь-якій машині.
    h2 = hashlib.sha256(text.encode("utf-8")).hexdigest()
    print(f"  Той самий вхід → той самий хеш? {h == h2}\n")

    # ── 1.3: Лавинний ефект ───────────────────────────────────────
    # Змінюємо ОДНУ літеру → бачимо зовсім інший хеш. Це фіча, не баг.
    similar = "Привіт, світе!"   # додали "!"
    h3 = hashlib.sha256(similar.encode("utf-8")).hexdigest()
    print(f"  Додали '!' до тексту:")
    print(f"    було:  {h[:32]}...")
    print(f"    стало: {h3[:32]}...")
    print(f"  Хеші різні: {h != h3}\n")

    # ── 1.4: Хеш реального user_id (наш сценарій) ─────────────────
    # У bot_log.csv ми пишемо user_id. Замість 458192038 могли б писати хеш.
    # Тоді з лога не можна дізнатись, ХТО конкретно робив запити —
    # але /stats все одно правильно порахує "унікальних користувачів"
    # (бо однакові id → однакові хеші → nunique() працює).
    user_id = 458192038
    hashed_id = hashlib.sha256(str(user_id).encode()).hexdigest()
    print(f"  Хешування user_id для логів:")
    print(f"    plain:  {user_id}")
    print(f"    sha256: {hashed_id[:32]}...\n")

    print("  💡 Хеш — це 'відбиток' даних. З відбитка не відновити дані,")
    print("     але можна перевірити, що дані такі самі, як були.\n")


